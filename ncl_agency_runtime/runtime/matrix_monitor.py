class SLOEngine:
    """Service Level Objective engine.

    Evaluates current health metrics against defined SLO targets
    and tracks error budget consumption.
    """

    DEFAULT_SLOS: ClassVar[list[dict[str, Any]]] = [
        {
            "name": "system_health_score",
            "target": 0.8,
            "window_hours": 24,
            "metric_source": "overall_score",
            "description": "Overall system health score must stay above 80%",
        },
        {
            "name": "pillar_availability",
            "target": 0.75,
            "window_hours": 168,
            "metric_source": "pillar_online_ratio",
            "description": "At least 75% of pillars should be online",
        },
        {
            "name": "checks_pass_rate",
            "target": 0.7,
            "window_hours": 24,
            "metric_source": "checks_pass_rate",
            "description": "At least 70% of health checks should pass",
        },
    ]

    def __init__(self, config: dict[str, Any] | None = None):
        self._slos: list[SLODefinition] = []
        self._load_slos(config)

    def _load_slos(self, config: dict[str, Any] | None) -> None:
        """Load SLO definitions from config or defaults."""
        slo_configs = (config or {}).get("slo", {})
        if slo_configs:
            for name, defn in slo_configs.items():
                self._slos.append(
                    SLODefinition(
                        name=name,
                        target=defn.get("target", 0.9),
                        window_hours=defn.get("window_hours", 24),
                        metric_source=defn.get("metric_source", name),
                        description=defn.get("description", ""),
                    )
                )
        else:
            for defn in self.DEFAULT_SLOS:
                self._slos.append(SLODefinition(**defn))

    @property
    def slo_definitions(self) -> list[SLODefinition]:
        return list(self._slos)

    def evaluate(
        self,
        current_metrics: dict[str, float],
        history: list[dict[str, Any]],
    ) -> list[SLOStatus]:
        """Evaluate all SLOs against current metrics and history."""
        results = []
        for slo in self._slos:
            metric_val = current_metrics.get(slo.metric_source, 0.0)
            # Calculate budget from historical values
            window_values = self._extract_window_values(
                history,
                slo.metric_source,
                slo.window_hours,
            )
            if window_values:
                avg_in_window = sum(window_values) / len(window_values)
            else:
                avg_in_window = metric_val

            # Budget: how much of the error budget remains
            # budget = 1.0 when avg == target, 0.0 when avg == 0
            if slo.target > 0:
                budget = min(1.0, max(0.0, avg_in_window / slo.target))
            else:
                budget = 1.0

            in_violation = metric_val < slo.target

            results.append(
                SLOStatus(
                    slo=slo,
                    current_value=round(metric_val, 4),
                    budget_remaining=round(budget, 4),
                    in_violation=in_violation,
                    samples=len(window_values),
                )
            )
        return results

    def _extract_window_values(
        self,
        history: list[dict[str, Any]],
        metric: str,
        window_hours: int,
    ) -> list[float]:
        """Pull metric values from history within the SLO window."""
        now = datetime.now(UTC)
        values = []
        for entry in history:
            ts_str = entry.get("ts", "")
            if not ts_str:
                continue
            try:
                ts = datetime.fromisoformat(ts_str)
                if (now - ts).total_seconds() <= window_hours * 3600:
                    val = entry.get(metric)
                    if val is not None:
                        values.append(float(val))
            except (ValueError, TypeError):
                continue
        return values


