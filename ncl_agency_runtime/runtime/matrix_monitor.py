def _collect_system_health(repo_root: Path) -> list[HealthCheckResult]:
    """Collect health checks from tools/system_health_check.py — adapted to HealthCheckResult."""
    results: list[HealthCheckResult] = []
    try:
        import importlib.util

        spec = importlib.util.spec_from_file_location(
            "system_health_check",
            str(repo_root / "tools" / "system_health_check.py"),
        )
        if spec and spec.loader:
            mod = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(mod)
            checker = mod.NCLHealthChecker(str(repo_root / "ncl_config.json"))

            # Run non-network checks only (avoid HTTP timeouts)
            safe_checks = [
                ("python_deps", checker.check_python_dependencies),
                ("directory_structure", checker.check_directory_structure),
                ("schemas", checker.check_schemas),
                ("golden_tasks", checker.check_golden_tasks),
                ("shortcuts_pack", checker.check_shortcuts_pack),
                ("agency_runtime", checker.check_agency_runtime),
                ("onedrop_setup", checker.check_onedrop_setup),
            ]

            for name, check_fn in safe_checks:
                try:
                    passed = check_fn()
                    detail = json.dumps(checker.results.get(name, {}))
                    results.append(
                        HealthCheckResult(
                            source=HealthSource.SYSTEM,
                            name=name,
                            passed=passed,
                            score=1.0 if passed else 0.0,
                            details=detail,
                        )
                    )
                except Exception as exc:
                    results.append(
                        HealthCheckResult(
                            source=HealthSource.SYSTEM,
                            name=name,
                            passed=False,
                            score=0.0,
                            details=f"Check error: {exc}",
                        )
                    )
    except Exception as exc:
        LOG.warning("System health collector failed: %s", exc)
        results.append(
            HealthCheckResult(
                source=HealthSource.SYSTEM,
                name="system_health_collector",
                passed=False,
                score=0.0,
                details=f"Collector failed: {exc}",
            )
        )
    return results


