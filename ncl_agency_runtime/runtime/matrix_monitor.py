class MatrixMonitorStore:
    """Persists Matrix Monitor snapshots and provides historical queries.

    Files:
        logs/matrix_latest.json    — most recent snapshot
        logs/matrix_history.ndjson — append-only history for trends
    """

    def __init__(self, log_dir: Path | None = None):
        self._log_dir = log_dir or (_REPO_ROOT / "ncl_agency_runtime" / "logs")
        self._log_dir.mkdir(parents=True, exist_ok=True)
        self._latest_path = self._log_dir / "matrix_latest.json"
        self._history_path = self._log_dir / "matrix_history.ndjson"

    def save(self, report: MatrixReport) -> Path:
        """Persist a MatrixReport snapshot."""
        data = report.to_dict()
        self._latest_path.write_text(
            json.dumps(data, indent=2, default=str) + "\n",
            encoding="utf-8",
        )
        # Append compact entry to history
        compact = {
            "ts": report.timestamp,
            "score": report.overall_score,
            "status": report.health_status,
            "passed": report.checks_passed,
            "total": report.checks_total,
            "slo_violations": report.slos_in_violation,
            "alerts": len(report.alerts),
        }
        with self._history_path.open("a", encoding="utf-8") as f:
            f.write(json.dumps(compact) + "\n")
        return self._latest_path

    def get_latest(self) -> dict[str, Any] | None:
        """Load the most recent snapshot."""
        if not self._latest_path.exists():
            return None
        return json.loads(self._latest_path.read_text(encoding="utf-8"))

    def get_history(self, max_entries: int = 100) -> list[dict[str, Any]]:
        """Load recent history entries."""
        if not self._history_path.exists():
            return []
        lines = self._history_path.read_text(encoding="utf-8").splitlines()
        entries = []
        for line in lines[-max_entries:]:
            line = line.strip()
            if line:
                entries.append(json.loads(line))
        return entries

    def get_trend(self, metric: str, max_entries: int = 50) -> list[dict[str, Any]]:
        """Extract a specific metric's trend from history."""
        history = self.get_history(max_entries)
        return [{"ts": h.get("ts", ""), "value": h.get(metric)} for h in history if metric in h]


