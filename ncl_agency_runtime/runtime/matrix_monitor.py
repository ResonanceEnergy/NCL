class AlertRouter:
    """Generates and routes alerts based on health transitions and SLO violations.

    Doctrine: Law 17 "Suspended terror" — the threat of escalation deters decay.
    """

    def __init__(self) -> None:
        self._alerts: list[AlertRecord] = []
        self._max_alerts = 100

    @property
    def active_alerts(self) -> list[AlertRecord]:
        return [a for a in self._alerts if not a.acknowledged]

    @property
    def all_alerts(self) -> list[AlertRecord]:
        return list(self._alerts)

    def evaluate(
        self,
        checks: list[HealthCheckResult],
        slo_statuses: list[SLOStatus],
        overall_score: float,
    ) -> list[AlertRecord]:
        """Generate alerts from current check results and SLO evaluations."""
        new_alerts: list[AlertRecord] = []

        # Alert on overall score degradation
        if overall_score < 0.3:
            new_alerts.append(
                AlertRecord(
                    severity=AlertSeverity.CRITICAL,
                    source="matrix_monitor",
                    title="System health CRITICAL",
                    details=f"Overall score {overall_score:.2f} is below 0.30 threshold",
                )
            )
        elif overall_score < 0.5:
            new_alerts.append(
                AlertRecord(
                    severity=AlertSeverity.ERROR,
                    source="matrix_monitor",
                    title="System health DEGRADED",
                    details=f"Overall score {overall_score:.2f} is below 0.50 threshold",
                )
            )
        elif overall_score < 0.7:
            new_alerts.append(
                AlertRecord(
                    severity=AlertSeverity.WARNING,
                    source="matrix_monitor",
                    title="System health FAIR",
                    details=f"Overall score {overall_score:.2f} is below 0.70 threshold",
                )
            )

        # Alert on SLO violations
        for slo_status in slo_statuses:
            if slo_status.in_violation:
                severity = AlertSeverity.ERROR
                if slo_status.budget_remaining < 0.2:
                    severity = AlertSeverity.CRITICAL
                elif slo_status.budget_remaining < 0.5:
                    severity = AlertSeverity.ERROR
                else:
                    severity = AlertSeverity.WARNING

                new_alerts.append(
                    AlertRecord(
                        severity=severity,
                        source="slo_engine",
                        title=f"SLO violation: {slo_status.slo.name}",
                        details=(
                            f"Current: {slo_status.current_value:.2f}, "
                            f"Target: {slo_status.slo.target:.2f}, "
                            f"Budget remaining: {slo_status.budget_remaining:.1%}"
                        ),
                    )
                )

        # Alert on critical check failures
        for check in checks:
            if not check.passed and check.score == 0.0:
                new_alerts.append(
                    AlertRecord(
                        severity=AlertSeverity.ERROR,
                        source=check.source.value,
                        title=f"Check failed: {check.name}",
                        details=check.details,
                    )
                )

        # Store and trim
        self._alerts.extend(new_alerts)
        if len(self._alerts) > self._max_alerts:
            self._alerts = self._alerts[-self._max_alerts :]

        return new_alerts

    def acknowledge(self, index: int) -> bool:
        """Acknowledge an alert by index."""
        if 0 <= index < len(self._alerts):
            self._alerts[index].acknowledged = True
            return True
        return False

    def clear_acknowledged(self) -> int:
        """Remove acknowledged alerts."""
        before = len(self._alerts)
        self._alerts = [a for a in self._alerts if not a.acknowledged]
        return before - len(self._alerts)


