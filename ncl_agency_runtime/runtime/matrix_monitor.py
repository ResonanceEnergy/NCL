def main() -> None:
    """Run Matrix Monitor from command line."""
    import sys

    logging.basicConfig(
        level=logging.INFO,
        format="[%(asctime)s] %(levelname)-8s %(name)s  %(message)s",
        datefmt="%Y-%m-%dT%H:%M:%S",
    )

    monitor = MatrixMonitorOrchestrator()
    report = monitor.collect_all()

    # Publish to bus
    monitor.publish_to_bus(report)

    # Print summary
    print(f"\n{'=' * 60}")
    print(f"  NCC MATRIX MONITOR — {report.health_status}")
    print(f"{'=' * 60}")
    print(f"  Overall Score : {report.overall_score:.1%}")
    print(f"  Checks Passed : {report.checks_passed}/{report.checks_total}")
    print(f"  SLO Violations: {report.slos_in_violation}")
    print(f"  Active Alerts : {len(report.alerts)}")
    print(f"  Uptime        : {report.uptime_s:.0f}s")
    print(f"{'=' * 60}")

    # Print tiles
    print("\n  DASHBOARD TILES")
    print(f"  {'-' * 56}")
    for tile in report.tiles:
        indicator = {"green": "[OK]", "yellow": "[!!]", "red": "[XX]", "grey": "[--]"}.get(tile.status, "[??]")
        print(f"  {indicator} {tile.title}: {tile.value} — {tile.subtitle}")

    # Print alerts
    if report.alerts:
        print(f"\n  ALERTS ({len(report.alerts)})")
        print(f"  {'-' * 56}")
        for alert in report.alerts:
            sev = alert.severity.value.upper()
            print(f"  [{sev}] {alert.title}: {alert.details}")

    # Print daily briefs
    brief_checks = [c for c in report.checks if c.name.startswith("brief_")]
    if brief_checks:
        print("\n  DAILY BRIEFS")
        print(f"  {'-' * 56}")
        for bc in brief_checks:
            indicator = "[OK]" if bc.passed else ("[!!]" if bc.score > 0 else "[XX]")
            print(f"  {indicator} {bc.details}")
            if bc.recommendation:
                print(f"        -> {bc.recommendation}")
        # Show paths to latest briefs
        brief_summary = getattr(monitor, "_brief_summary", {})
        for btype in ("az", "csuite"):
            info = brief_summary.get(btype, {})
            if info.get("md"):
                print(f"        MD:  {info['md']}")
            if info.get("pdf"):
                print(f"        PDF: {info['pdf']}")

    # Print SLOs
    print("\n  SLO STATUS")
    print(f"  {'-' * 56}")
    for slo_status in report.slo_statuses:
        indicator = "[OK]" if not slo_status.in_violation else "[XX]"
        print(
            f"  {indicator} {slo_status.slo.name}: "
            f"{slo_status.current_value:.1%} "
            f"(target: {slo_status.slo.target:.1%}, "
            f"budget: {slo_status.budget_remaining:.0%})"
        )

    print("\n  Report saved to: ncl_agency_runtime/logs/matrix_latest.json")
    print(f"{'=' * 60}\n")

    # Exit code: 0 if healthy, 1 if degraded/critical
    sys.exit(0 if report.overall_score >= 0.5 else 1)


if __name__ == "__main__":
    main()
