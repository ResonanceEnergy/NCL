def _collect_ncc_governance(repo_root: Path) -> tuple[list[HealthCheckResult], dict[str, Any]]:
    """Collect health from PillarRegistry and return pillar summary."""
    results: list[HealthCheckResult] = []
    pillar_summary: dict[str, Any] = {}
    try:
        from ncl_agency_runtime.runtime.pillar_registry import (
            PillarRegistry,
            bootstrap_registry,
        )

        registry = PillarRegistry.get_instance()
        # Bootstrap if empty
        if not registry.list_pillars():
            bootstrap_registry()

        # Promote bootstrapping pillars to online via heartbeat —
        # the Matrix Monitor confirming their presence IS the heartbeat.
        for pillar in registry.list_pillars():
            registry.heartbeat(pillar.pillar_id)

        summary = registry.health_summary()
        pillar_summary = summary

        total = summary.get("total_pillars", 0)
        online = summary.get("online", 0)
        degraded = summary.get("degraded", 0)

        # Overall pillar health check
        if total > 0:
            online_ratio = online / total
        else:
            online_ratio = 0.0

        results.append(
            HealthCheckResult(
                source=HealthSource.NCC_GOVERNANCE,
                name="pillar_availability",
                passed=online_ratio >= 0.5,
                score=online_ratio,
                details=f"{online}/{total} pillars online, {degraded} degraded",
            )
        )

        # Triad check
        triad_online = registry.triad_online()
        results.append(
            HealthCheckResult(
                source=HealthSource.NCC_GOVERNANCE,
                name="triad_online",
                passed=triad_online,
                score=1.0 if triad_online else 0.0,
                details=f"Triad status: {registry.triad_status()}",
                recommendation="" if triad_online else "Bring offline triad pillars online",
            )
        )

        # Per-pillar checks
        for pid, info in summary.get("pillars", {}).items():
            status = info.get("status", "offline")
            is_online = status in ("online", "bootstrapping")
            results.append(
                HealthCheckResult(
                    source=HealthSource.NCC_GOVERNANCE,
                    name=f"pillar_{pid}",
                    passed=is_online,
                    score=1.0 if status == "online" else (0.5 if status in ("degraded", "bootstrapping") else 0.0),
                    details=f"{info.get('name', pid)}: {status} ({info.get('capabilities', 0)} capabilities)",
                )
            )

    except Exception as exc:
        LOG.warning("NCC governance collector failed: %s", exc)
        results.append(
            HealthCheckResult(
                source=HealthSource.NCC_GOVERNANCE,
                name="governance_collector",
                passed=False,
                score=0.0,
                details=f"Collector failed: {exc}",
            )
        )
    return results, pillar_summary


def _collect_fpc_health(repo_root: Path) -> list[HealthCheckResult]:
    """Collect health checks from FPC intelligence scanner."""
    results: list[HealthCheckResult] = []
    try:
        from ncl_agency_runtime.runtime.fpc_integration import scan_fpc_health

        gaps = scan_fpc_health(repo_root)
        if not gaps:
            results.append(
                HealthCheckResult(
                    source=HealthSource.FPC_INTELLIGENCE,
                    name="fpc_health",
                    passed=True,
                    score=1.0,
                    details="FPC intelligence layer healthy — no gaps detected",
                )
            )
        else:
            # Convert gaps to individual check results
            high_severity = sum(1 for g in gaps if g.get("severity") == "high")
            score = max(0.0, 1.0 - (high_severity * 0.2) - ((len(gaps) - high_severity) * 0.05))
            results.append(
                HealthCheckResult(
                    source=HealthSource.FPC_INTELLIGENCE,
                    name="fpc_health",
                    passed=high_severity == 0,
                    score=round(score, 2),
                    details=f"{len(gaps)} gaps detected ({high_severity} high severity)",
                    recommendation="Review FPC intelligence gaps",
                )
            )
            for gap in gaps[:5]:  # Top 5 gaps as individual checks
                results.append(
                    HealthCheckResult(
                        source=HealthSource.FPC_INTELLIGENCE,
                        name=f"fpc_gap_{gap.get('category', 'unknown')}",
                        passed=False,
                        score=0.3 if gap.get("severity") == "medium" else 0.0,
                        details=gap.get("description", ""),
                    )
                )
    except Exception as exc:
        LOG.warning("FPC health collector failed: %s", exc)
        results.append(
            HealthCheckResult(
                source=HealthSource.FPC_INTELLIGENCE,
                name="fpc_collector",
                passed=False,
                score=0.0,
                details=f"Collector failed: {exc}",
            )
        )
    return results


