class HealthSource(StrEnum):
    """Source subsystem that produced a health check."""

    SYSTEM = "system"
    SELF_CHECK = "self_check"
    NCC_GOVERNANCE = "ncc_governance"
    FPC_INTELLIGENCE = "fpc_intelligence"
    AGENT = "agent"
    ENDPOINT = "endpoint"
    AUTONOMOUS_OPS = "autonomous_ops"
    INTELLIGENCE = "intelligence"
    FORECASTING = "forecasting"
    MEMORY = "memory"
    COMMUNICATION = "communication"
    HELIX_NEWS = "helix_news"


class AlertSeverity(StrEnum):
    """Alert severity levels for the Matrix Monitor."""

    INFO = "info"
    WARNING = "warning"
    ERROR = "error"
    CRITICAL = "critical"


@dataclass
class HealthCheckResult:
    """Universal health check result — the common protocol all subsystems produce."""

    source: HealthSource
    name: str
    passed: bool
    score: float  # 0.0 to 1.0
    details: str = ""
    recommendation: str = ""
    timestamp: str = field(default_factory=lambda: datetime.now(UTC).isoformat())

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["source"] = self.source.value
        return d


@dataclass
class SLODefinition:
    """Service Level Objective definition."""

    name: str
    target: float  # target value (e.g. 0.9 for 90%)
    window_hours: int  # evaluation window
    metric_source: str  # which metric to track
    description: str = ""

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


@dataclass
class SLOStatus:
    """Current SLO evaluation result."""

    slo: SLODefinition
    current_value: float
    budget_remaining: float  # 1.0 = full budget, 0.0 = exhausted
    in_violation: bool
    samples: int
    evaluated_at: str = field(default_factory=lambda: datetime.now(UTC).isoformat())

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        return d


@dataclass
class AlertRecord:
    """An alert generated by the Matrix Monitor."""

    severity: AlertSeverity
    source: str
    title: str
    details: str
    timestamp: str = field(default_factory=lambda: datetime.now(UTC).isoformat())
    acknowledged: bool = False

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["severity"] = self.severity.value
        return d


@dataclass
class DashboardTile:
    """Data for a single dashboard tile."""

    tile_type: str  # "progress" | "slo" | "health" | "pillar" | "adoption"
    title: str
    value: Any
    status: str  # "green" | "yellow" | "red" | "grey"
    subtitle: str = ""
    updated_at: str = field(default_factory=lambda: datetime.now(UTC).isoformat())

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


@dataclass
class MatrixReport:
    """Complete Matrix Monitor snapshot."""

    timestamp: str
    overall_score: float
    health_status: str  # EXCELLENT / GOOD / FAIR / DEGRADED / CRITICAL
    checks: list[HealthCheckResult]
    checks_passed: int
    checks_total: int
    slo_statuses: list[SLOStatus]
    slos_in_violation: int
    alerts: list[AlertRecord]
    tiles: list[DashboardTile]
    pillar_summary: dict[str, Any]
    uptime_s: float

    def to_dict(self) -> dict[str, Any]:
        return {
            "timestamp": self.timestamp,
            "overall_score": self.overall_score,
            "health_status": self.health_status,
            "checks_passed": self.checks_passed,
            "checks_total": self.checks_total,
            "checks": [c.to_dict() for c in self.checks],
            "slo_statuses": [s.to_dict() for s in self.slo_statuses],
            "slos_in_violation": self.slos_in_violation,
            "alerts": [a.to_dict() for a in self.alerts],
            "tiles": [t.to_dict() for t in self.tiles],
            "pillar_summary": self.pillar_summary,
            "uptime_s": self.uptime_s,
        }


